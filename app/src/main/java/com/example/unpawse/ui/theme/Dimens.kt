package com.example.unpawse.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Spacing tokens from DESIGN.md (8px rhythmic scale). Centralised so screens stay consistent.
 */
object Dimens {
    /** Horizontal breathing room from the device edges. */
    val ScreenHMargin = 20.dp

    /** Vertical gap between stacked cards. */
    val StackGap = 12.dp

    /** Internal padding for cards / high-level containers. */
    val CardPadding = 24.dp

    /** Gap between side-by-side items in a row. */
    val Gutter = 16.dp

    /** Base unit of the 8px scale. */
    val Base = 8.dp
}
